# 通用运算
# '+' 运算符
# 加法操作不支持字典和集合
list1 = [1, 2, 3]
list2 = [3, 4, 5]
list_add = list1 + list2
print(list_add)
print(type(list_add))

tuple1 = (1, 2, 3)
tuple2 = (3, 4, 5)
tuple_add = tuple1 + tuple2
print(tuple_add)
print(type(tuple_add))

# '*' 运算符
# 乘法操作也不支持字典和集合
list1 = [1, 2, 3]
print(list1 * 5)

tuple1 = (1, 2, 3)
print(tuple1 * 5)

